package com.arrayPackage;

public class SwitchCaseWithExamples {

	public static void main(String[] args) {
		int department = 10;
		switch (department) {
		case 1:
			System.out.println("Present 1");
			break;
		case 2:
			System.out.println("Present 2");
			break;
		case 3:
			System.out.println("Present 3");
			break;
		case 5:
			System.out.println("Present 5");
			break;
		case 4:
			System.out.println("Present 4");
			break;
		default:
			System.out.println("Not present");
		}
	}
}